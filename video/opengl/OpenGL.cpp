		yyLogWriteInfo("Renderer: %s\n", renderer);
	gglClearDepth(1.0f);
	gglEnable(GL_DEPTH_TEST);
	gglFrontFace(GL_CW);
	gglViewport(0, 0, m_window->m_size.x, m_window->m_size.y);

	//yyImage whiteImage;
	//whiteImage.m_width  = 2;
