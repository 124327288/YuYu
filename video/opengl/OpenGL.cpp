	int pixelFormat[1];
	unsigned int formatCount;
	auto result = gwglChoosePixelFormatARB(m_windowDC, attributeListInt, NULL, 1, pixelFormat, &formatCount);
	if(result != 1)
	{
		YY_PRINT_FAILED;
		return false;
	}

